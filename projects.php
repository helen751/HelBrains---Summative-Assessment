<script>
  (function () {
    const overlay = document.getElementById('imageCanvas');
    const preview = document.getElementById('imageCanvasPreview');
    const closeBtn = document.getElementById('closeImageCanvas');

    function openCanvas(imgSrc, imgAlt) {
      preview.src = imgSrc;
      preview.alt = imgAlt || 'Full image preview';
      overlay.style.display = 'flex';
      document.body.style.overflow = 'hidden';
    }

    function closeCanvas() {
      overlay.style.display = 'none';
      preview.src = '';
      document.body.style.overflow = '';
    }

    // Open on image click
    document.querySelectorAll('.js-open-image').forEach((a) => {
      a.addEventListener('click', function (e) {
        e.preventDefault();
        const img = this.querySelector('img');
        if (!img) return;
        openCanvas(img.getAttribute('src'), img.getAttribute('alt'));
      });
    });

    // Close actions
    closeBtn.addEventListener('click', closeCanvas);

    overlay.addEventListener('click', function (e) {
      // click outside the image closes
      if (e.target === overlay) closeCanvas();
    });

    document.addEventListener('keydown', function (e) {
      if (e.key === 'Escape' && overlay.style.display === 'flex') {
        closeCanvas();
      }
    });
  })();
</script>